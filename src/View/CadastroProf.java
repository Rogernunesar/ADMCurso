    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String[]{"ID","Nome","Email","Valor Hora", "Formação"};
        conexao.conexao();
        conexao.executaSql(sql);
        
         try {
             conexao.result.first();
             do{
                dados.add(new Object[]{conexao.result.getInt("idProf"),conexao.result.getString("nomeProf"),conexao.result.getString("emailProf"), conexao.result.getString("valorHora"), conexao.result.getString("formacao")});  
             }while(conexao.result.next());
         } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Digite outro nome");
         }
         ModeloTabela modelo = new ModeloTabela(dados, colunas);
         
         JTableProfessor.setModel(modelo);
         JTableProfessor.getColumnModel().getColumn(0).setPreferredWidth(35);
         JTableProfessor.getColumnModel().getColumn(0).setResizable(false);
         JTableProfessor.getColumnModel().getColumn(1).setPreferredWidth(210);
         JTableProfessor.getColumnModel().getColumn(1).setResizable(false);
         JTableProfessor.getColumnModel().getColumn(2).setPreferredWidth(210);
         JTableProfessor.getColumnModel().getColumn(2).setResizable(false);
         JTableProfessor.getColumnModel().getColumn(3).setPreferredWidth(70);
         JTableProfessor.getColumnModel().getColumn(3).setResizable(false);
         JTableProfessor.getColumnModel().getColumn(4).setPreferredWidth(113);
         JTableProfessor.getColumnModel().getColumn(4).setResizable(false);
         JTableProfessor.getTableHeader().setReorderingAllowed(false);
         JTableProfessor.setAutoResizeMode(JTableProfessor.AUTO_RESIZE_OFF);
         JTableProfessor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
         conexao.desconecta();
         
         
    }

