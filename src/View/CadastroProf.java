    private void JTableProfessorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableProfessorMouseClicked
        String nomeProf = ""+JTableProfessor.getValueAt(JTableProfessor.getSelectedRow(), 1);
        conexao.conexao();
        conexao.executaSql("select * from professores where nomeProf = '"+nomeProf+"'");
        
         try {
             conexao.result.first();
             jTextFIdProfessor.setText(String.valueOf(conexao.result.getInt("idProf")));
             jTextFNome.setText(conexao.result.getString("nomeProf"));
             jTextFEmail.setText(conexao.result.getString("emailProf"));
             jTextFHoraAula.setText(conexao.result.getString("valorHora"));
             jTextFCurso.setText(conexao.result.getString("formacao"));
         } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null,"Erro ao selecionar dados"+ex); 
         }
        conexao.desconecta();
        BotaoPesquisar();
    }//GEN-LAST:event_JTableProfessorMouseClicked

