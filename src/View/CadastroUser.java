    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        mod.setPesquisa(jTextFPesquisa.getText());
        ModeloUser model = controle.buscarUser(mod);
        
        jTextFIdUser.setText(String.valueOf(model.getIduser()));
        jTextFUser.setText(model.getNomeuser());
        jPasswordFieldSenha.setText(model.getSenhauser());
        jPasswordFieldConfSenha.setText(model.getSenhauser());
        jComboBoxUser.setSelectedItem(model.getTipouser());
        jComboBoxUser.setEnabled(false);
        //preencherTabela("select * from alunos where nomeAluno like'%"+mod.getPesquisa()+"%'");
       
        BotaoPesquisar();

    }//GEN-LAST:event_jbPesquisarActionPerformed

