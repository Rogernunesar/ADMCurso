    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        if(jTextFUser.getText().isEmpty()){//nao deixar o campos em branco
            JOptionPane.showMessageDialog(null, "Preencha o campo USUARIO para continuar!");
            jTextFUser.requestFocus();
        }else if(jPasswordFieldSenha.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha o campo SENHA para continuar!!");
                jPasswordFieldSenha.requestFocus();
        }else if(jPasswordFieldConfSenha.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha o campo CONFIRMA SENHA para continuar!");
                jPasswordFieldConfSenha.requestFocus();
        }else 
            if(jPasswordFieldSenha.getText().equals(jPasswordFieldConfSenha.getText())){
              if(flag == 1)
                {
                mod.setNomeuser(jTextFUser.getText());
                mod.setSenhauser(jPasswordFieldSenha.getText());
                mod.setTipouser((String)jComboBoxUser.getSelectedItem());
                controle.Salvar(mod);
                jTextFUser.setText("");
                jPasswordFieldSenha.setText("");
                jPasswordFieldConfSenha.setText("");
                
                jTextFUser.setEnabled(false);
                jPasswordFieldSenha.setEnabled(false);
                jPasswordFieldConfSenha.setEnabled(false);
                
                BotaoSalvarflag1();
                //preencherTabela("select * from alunos order by nomeAluno");//para atualizar a tabela

            }else
            {
                mod.setNomeuser(jTextFUser.getText());
                mod.setSenhauser(jPasswordFieldSenha.getText());
                mod.setTipouser((String)jComboBoxUser.getSelectedItem());
                controle.Editar(mod);
                jTextFIdUser.setText("");
                jTextFUser.setText("");
                jPasswordFieldSenha.setText("");
                jPasswordFieldConfSenha.setText("");
                
                jTextFUser.setEnabled(false);
                jPasswordFieldSenha.setEnabled(false);
                jPasswordFieldConfSenha.setEnabled(false);
                jComboBoxUser.setEnabled(false);
                BotaoSalvarflag2();
               // preencherTabela("select * from alunos order by nomeAluno");

            }  
        }else {
              JOptionPane.showMessageDialog(rootPane, "As senhas não conferem");
        }
                   
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int resposta = 0;
        resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir");
        if(resposta == JOptionPane.YES_OPTION)
            mod.setIduser(Integer.valueOf((jTextFIdUser.getText())));
            controle.Excluir(mod);
            jTextFIdUser.setText("");
            jTextFUser.setText("");
            jPasswordFieldSenha.setText("");
            jPasswordFieldConfSenha.setText("");
            
            BotaoExcluir();   
           // preencherTabela("select * from alunos order by nomeAluno");
    }//GEN-LAST:event_jbExcluirActionPerformed

